<h2>System requirements</h2>
<ul>
<li>Windows 98, Me, 2000, XP (tested under Win XP and Win 2000)</li>
<li>DirectX 8.1 or later</li>
<li>Intel Pentium III compatible CPU, 800Mhz or faster</li>
<li>128MB RAM</li>
<li>32MB DirectX compatible video card that supports 32bit color depth (tested with GeForce2 MX, GeForce 3, nForce, Matrox Millenium G450)</li>
<li>DirectX compatible sound card</li>
<li>about 30MB free on your HDD</li>
</ul>

<h2>Installation</h2>

<ul>
 <li>Download and run the installation file from this web (version 2.4, full install).</li>
 <li>If you already have some older version installed, please do not download the full installation pack but use the patch instead (version 2.4, update from version 2.3 or 2.2).</li>
 <li>Launch KRKAL by running <i>krkal.exe.</i></li>
 <li>The documentation can be found in the directory <i>Dokumentace</i> (or Doc in version 2.2)</li>
 <li>To uninstall KRKAL either run the uninstaller, or just remove the entire KRKAL directory; KRKAL does not store any information in the Windows registry.</li>
</ul>

