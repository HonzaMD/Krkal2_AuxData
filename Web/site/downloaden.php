<h4>Improvements from version 2.3</h4>
<ul>
<li>Five new levels.</li>
<li>The kernel is now able to save and restore the game (implemented the savepoint).</li>
<li>New objects added to the game Krkal - Savepoint, Ice Ball, Teleport2 (can teleport all moving objects).</li>
<li>New graphics (bump floor, save point, tire, nitroglycerin, electrodes, and ice ball).</li>
<li>Fixed editor bug - editor did not load the shortcuts.</li>
<li>Editor - many partial changes made for easier use. Improved refresh object function.</li>
<li>Added new types for KSID names: ObjectVoid and AutoVoid.</li>
<li>Improved the registry efficiency, which enables faster loading and storing of scripts and levels.</li>
<li>Documentation updates (Description of the game Krkal, How to write scripts, Kernel services).</li>
<li>More minor improvements.</li>
</ul>

